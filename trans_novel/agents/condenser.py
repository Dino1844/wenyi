"""核心浓缩 Agent（强档）。

接收章节原文 + 角色定位 + 概念图 + 全书结构 + 前文上下文，
输出浓缩文本。按书籍类型选择不同浓缩策略。
"""

from __future__ import annotations

from typing import Any

from .base import Agent

# ── 按类型的浓缩规则 ─────────────────────────────────────────────────────────

RULES_ARGUMENTATIVE = """\
【浓缩规则（论证类）】
你的任务不是"总结这一章的观点"，而是让读者"跟着论证走一遍"。
1. 保留推理的推进感——每一步必须从上一步逼出来，不能跳跃。
2. 保留核心思想实验/类比的完整展开（这是"泵"本身）。
3. 去掉：对同行论文的引用和回应、"正如我在第X章所说"式回顾、
   同一论点的第二第三个补充例子（保留最有力的一个）。
4. 概念首次出现时，用一句话给出本书中的定义，不假设读者已知。
5. 不用"作者认为""作者指出"这类转述语——让论证自己说话。
6. 结尾保留原文的"落点"（通常是一个反问或一个不舒服的结论）。\
"""

RULES_NARRATIVE = """\
【浓缩规则（叙事类）】
你的任务不是"缩写故事"，而是保留读者经历这个故事的情感弧线。
1. 保留关键场景的完整性（对话、动作、转折）。
2. 用一两句过渡连接场景之间的空白，不铺陈。
3. 保留人物的声音——关键对话原样保留，不改为间接引语。
4. 去掉：环境描写的细节铺陈、次要支线的展开、重复的情感渲染。
5. 保留"意外"——叙事中让读者吃惊的时刻必须完整保留。\
"""

RULES_KNOWLEDGE = """\
【浓缩规则（知识类）】
你的任务不是"提取知识点"，而是让读者经历"概念层层展开"的过程。
1. 每个核心概念：给出定义 + 一个最有力的例子/类比。
2. 保留概念之间的连接——"为什么理解A之后才能理解B"。
3. 去掉：同一概念的多个重复举例、"让我们回顾一下"式段落、
   过渡性废话（"接下来我们将讨论……"）。
4. 保留"啊哈时刻"——概念突然变得清晰的那个例子或类比。\
"""

RULES_MAP = {
    "argumentative": RULES_ARGUMENTATIVE,
    "narrative": RULES_NARRATIVE,
    "knowledge": RULES_KNOWLEDGE,
    "mixed": RULES_ARGUMENTATIVE,  # 混合类默认用论证规则，章节级可覆盖
}

# ── 提示词 ───────────────────────────────────────────────────────────────────

# TODO(极简版 prompt A/B 测试): 下面的 _SYSTEM 较冗长，已草拟一版极简版（约 150 字），
#   待用同一批章节（如《直觉泵》ch73-74）做 A/B 对比，验证"极简版是否能在不损失
#   流畅度与论证保留度的前提下达到同等效果"。若效果相当，优先采用极简版以省 token。
#   极简版草稿：
#     你是书本浓缩师。浓缩≠概括：让读者跟着论证走一遍，只删冗余（重复例子、学术脚注、
#     回顾性套话），不替读者下结论。
#     用地道中文重写，而非压缩译文：删掉路标词（"拿X类比/同样/此外/首先其次"），
#     让类比自然浮现；少用"被"字句和连环"的"；长短句交错。
#     输出一段连续流畅的文字（非列表），约原文 $target_ratio%。
#     仅输出 JSON：{"condensed":"..."}
_SYSTEM = """\
你是一位书本短写师。你的工作不是"压缩"，更不是写摘要，而是把这一章**重写**得更短，
同时让读者完整地"跟着论证走一遍"。

短写 ≠ 压缩 ≠ 概括。
- 概括是告诉读者"这章讲了什么"——你不要这么做。
- 压缩是 proportionally 砍掉内容——你也不要这么做。
- 短写是：推理的每一步都完整保留、环环相扣，一个步骤都不能丢；
  你只删掉冗余——重复的论证、第二个第三个补充例子（留最有力的一个）、
  学术脚注、文献回应、"如我在第X章所说"式回顾、过渡废话。
  然后用更精炼的句子把保留下来的论证重新讲一遍。

最高优先级：思维链必须完整。
- 论证的每一步都要在，且每一步都要从上一步自然推出，读者不能感到跳跃。
- 宁可写得长一点，也不能为了凑比例而砍掉推理步骤、让思维链断裂。
- 核心思想实验/类比（这是"泵"本身）要完整展开，不能只剩一个名字。
- 概念首次出现时，用一句话给出本书中的定义。

中文表达：
- 原文多半是译文体。你要用地道、自然的中文把意思重新讲一遍，而不是压缩译文。
- 目标是流畅的书面语——像编辑精良的中文随笔或优质非虚构译文，
  绝不是口语、聊天或"老北京话"。戒掉碎嘴腔："这话谁也没说过""何必扯上什么"
  "不切实际""太牵强"这类短促碎句不要堆在一起。
- 以结构完整的句子为主体，让推理在句内连贯展开；短句只偶尔用于收束或点睛。
- 消灭路标词和元叙述："拿X做类比""同样""此外""首先/其次""让我们看""本段讨论"——
  让思路靠自身惯性过渡，不靠连接词报幕。
- 但推理的因果与转折不能丢：该有"因为/所以/然而/也就是说"的地方要保留。
- 戒掉翻译腔：少用被动式（"它被定义为…"写成"它就是…"），避免连续三个"的"的定语链。

输出要求：
- 输出一段连续的、流畅的文本（不是要点列表）。
- 参考长度：原文的 $target_ratio% 左右。这只是参考——思维链的完整性永远优先于比例，
  需要更长就写更长，不要为凑数而牺牲论证。
- 读起来应该像一条河流，而非一份报告。
- 不用"作者认为""作者指出"这类转述语，让论证自己说话。
- 结尾保留原文的"落点"（通常是一个反问或一个不舒服的结论）。
- 仅输出 JSON：{"condensed": "短写后的文本"}\
"""

_USER = """\
【全书结构】
$structure_brief

$rules

—— 以上为全书稳定信息；以下为本章专属内容 ——

【本章角色】
$chapter_role

【概念图（本章涉及）】
$concepts

【前文浓缩（最近，保持衔接）】
$context

【章节原文】
$source

请将以上章节浓缩为一段流畅的文本。\
"""


class Condenser(Agent):
    """核心浓缩 Agent：按书籍类型将章节原文浓缩为流畅文本。"""

    def condense_chapter(
        self,
        source_text: str,
        structure_brief: str = "",
        chapter_role: str = "",
        concepts: str = "",
        context: str = "",
        book_type: str = "argumentative",
        target_ratio: float = 0.2,
    ) -> str:
        """浓缩单章。

        Args:
            source_text: 章节原文。
            structure_brief: 全书结构简报。
            chapter_role: 本章角色定位简报。
            concepts: 本章涉及的概念及定义。
            context: 前文浓缩文本尾部（滚动上下文）。
            book_type: 书籍类型（决定浓缩规则）。
            target_ratio: 目标浓缩比例。

        Returns:
            浓缩后的文本。
        """
        from string import Template

        rules = RULES_MAP.get(book_type, RULES_ARGUMENTATIVE)
        system = Template(_SYSTEM).safe_substitute(
            target_ratio=int(target_ratio * 100),
        )
        user = Template(_USER).safe_substitute(
            source=source_text,
            structure_brief=structure_brief or "（暂无）",
            chapter_role=chapter_role or "（暂无）",
            concepts=concepts or "（暂无）",
            context=context or "（本章为开头，无前文）",
            rules=rules,
        )
        data = self._ask_json(system, user, tier="strong", default={})
        if isinstance(data, dict):
            return str(data.get("condensed", "")).strip()
        if isinstance(data, str):
            return data.strip()
        return ""
